public class ifelse {
    public static void main(String args[]) {
        int x = 8;
        int y = 7;
        int z = 6;

        if (x > y && y > z) {
            System.out.println("The value of x is greater than the values of both x and y ");
        } else {
            System.out.println("The value of x is not greater than the values of both y and z ");
        }
    }

}
